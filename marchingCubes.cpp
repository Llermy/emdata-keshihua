void MarchingCuber::setup(VolumeData *data, float dataThreshold)
{
    this->data = data;
    this->dataThreshold = dataThreshold;
    this->edgeLength = MC_SIZE / data->size[0];
}

/**
 * 从对象变量data的数据进行Marching Cubes算法。
 * 
 * triTableIndices包含每个体素的对应triTable的索引
 * vertices包含算法结果顶点
 * 
 * 返回值：vertices的数量
 */
int MarchingCuber::polygonize(float **vertices)
{
    // 先计算每个体素的对应triTable行
    int sizex = this->data->size[0]-1;
    int sizey = this->data->size[1]-1;
    int sizez = this->data->size[2]-1;
    int indNumber = sizex * sizey * sizez;
    int *triTableIndices = new int[indNumber];
    int totalNumVerts = 0;

    for(int z = 0; z < sizez; z++)
    {
        for(int y = 0; y < sizey; y++)
        {
            for(int x = 0; x < sizex; x++)
            {
                int tableIndex = this->voxelToTableIndex(x, y, z);
                triTableIndices[x + y*sizex + z*sizex*sizey] = tableIndex;
                totalNumVerts += this->tableIndexVertNum(tableIndex);
            }
        }
    }

    // 从每个triTable行计算对应的三角形及其法向量
    *vertices = new float[totalNumVerts*6]; // 顶点数量 * 3 顶点坐标成分 + 1 法向量
    int currentVertexF = 0;

    for(int z = 0; z < sizez; z++)
    {
        for(int y = 0; y < sizey; y++)
        {
            for(int x = 0; x < sizex; x++)
            {
                int arrayInd = x + y*sizex + z*sizex*sizey;
                
                for(int i = 0; i < 16; i += 3) {
                    if(triTable[triTableIndices[arrayInd]][i] < 0) {
                        break;
                    } else {
                        glm::vec3 points[3];
                        for(int j = 0; j < 3; j++) {
                            points[j] = interpolateEdge(
                                triTable[triTableIndices[arrayInd]][j+i], // edge index
                                x, y, z
                            );
                        }

                        glm::vec3 normal = glm::normalize(
                            glm::cross(
                                points[1] - points[0],
                                points[2] - points[0]
                        ));
                        for(int j = 0; j < 3; j++) {
                            (*vertices)[currentVertexF] = points[j].x;
                            (*vertices)[currentVertexF+1] = points[j].y;
                            (*vertices)[currentVertexF+2] = points[j].z;
                            currentVertexF += 3;

                            (*vertices)[currentVertexF] = normal.x;
                            (*vertices)[currentVertexF+1] = normal.y;
                            (*vertices)[currentVertexF+2] = normal.z;
                            currentVertexF += 3;
                        }
                    }
                }
            }
        }
    }

    delete[] triTableIndices;
    return totalNumVerts;
}

// 计算某个体素对应的triTable行
int MarchingCuber::voxelToTableIndex(int x, int y, int z)
{
    float cube[8] = {
        this->data->get(x, y+1, z),
        this->data->get(x+1, y+1, z),
        this->data->get(x+1, y+1, z+1),
        this->data->get(x, y+1, z+1),
        this->data->get(x, y, z),
        this->data->get(x+1, y, z),
        this->data->get(x+1, y, z+1),
        this->data->get(x, y, z+1)
    };

    int refIndex = 0;
    for(int i = 0; i < 8; i++) {
        if(cube[i] > this->dataThreshold)
            refIndex |= 1 << i;
    }
    return refIndex;
}

/**
 * 计算一个体素的edge对应的顶点
 * 
 */
glm::vec3 MarchingCuber::interpolateEdge(int edgeIndex, int x, int y, int z)
{
    // 计算出该体素的0，0，0的corner的位置
    glm::vec3 corner0(this->edgeLength*x, this->edgeLength*y, this->edgeLength*z);
    corner0 -= glm::vec3(
        this->edgeLength*this->data->size[0]/2,
        this->edgeLength*this->data->size[1]/2,
        this->edgeLength*this->data->size[2]/2);

    // 查询edgeIndex对应的体素的corner
    PosDuplet edgeCorners = cornerIndexFromEdge[edgeIndex];

    // 计算出edge的两个端点的位置
    glm::vec3 pos1(
        corner0.x + edgeCorners.p1[0]*this->edgeLength,
        corner0.y + edgeCorners.p1[1]*this->edgeLength,
        corner0.z + edgeCorners.p1[2]*this->edgeLength
    );
    glm::vec3 pos2(
        corner0.x + edgeCorners.p2[0]*this->edgeLength,
        corner0.y + edgeCorners.p2[1]*this->edgeLength,
        corner0.z + edgeCorners.p2[2]*this->edgeLength
    );

    // 按照这两个点和dataThreshold的关系，插值edge对应的顶点
    float val1 = this->data->get(x + edgeCorners.p1[0], y + edgeCorners.p1[1], z + edgeCorners.p1[2]);
    float val2 = this->data->get(x + edgeCorners.p2[0], y + edgeCorners.p2[1], z + edgeCorners.p2[2]);
    float interp = (this->dataThreshold - val1) / (val2 - val1);
    
    return pos1 * interp + pos2 * (1-interp);
}

int MarchingCuber::tableIndexVertNum(int index)
{
    int num;
    for(num = 0; num < 16; num++) {
        if(triTable[index][num] < 0) {
            break;
        }
    }
    return num;
}

